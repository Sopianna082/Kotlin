/**
 * Tipe Data Character
 * - Data karakter (huruf) dikotlin direpresentasikan oleh tipe Char
 * - Untuk Membuat data char, sikotlin kita bisa menggunakan tanda '(petik satu)
 */

fun main() {
    //isi dari character bukan hanya huruf tunggal, bisa juga angka
    //tetapi tetap menggunakan tanda petik satu
    var c: Char = '3'
    var e: Char = 'E'
    var grade: Char = 'A'

    println(c)
    println(e)
    print(grade)
}