/**
 * OOP
 * Class itu adalah blueprint atau design.
 * Contoh : desain mobil, dari desain ini akan dapat dibuat banyak objek berbagai jenis mobil
 *
 * Object adalah instances class / implementasi dari class
 * Contoh : Object mobil sport, mobil, listrik
 *
 * Constructor , ketika membuat object maka constructor akan dijalankan juga
 * Didalam class kita bisa memmbuat Constructor yang mirip dengan function yang akan dipanggil
