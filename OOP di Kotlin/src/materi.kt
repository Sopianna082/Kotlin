 *
 * Function
 * Didalam class, kita bisa mendeklarasikan Function yang menjadi behavior
 *
 * Inheritance (Pewarisan), misalkan ada class Parent dan class Child
 * Class Child akan mewarisi semua function / behaviour yang ada di class Parent
 * Class Parent bisa memiliki banyak child
 * unutk membuat class menjadi parent menggunakan kata kunci open
 *
 * Hak akses / Access Modifier
 * Public -> Hak akses yang dpat diakses dari manapun
 * Private -> Hak akses yang dapat diakses dari dalam classnya saja
 * Protected -> Hak akses bisa diakses class itu sendiri dan class turunanya
 * Internal -> Jarang dipakai, hak akses yang cakupannya terbatas dalam satu modul
 * jika beda modul tidak dapat diakses lagi
 *
 * Abstract Class
 * Bisa membuat class yang menjadikan abstract class
 * Class abtsract, tidak bisa membuat object
 * Hanya bisa diturunkan
 *
 * Interface, mirip dengan abstract
 * Interface istilahnya bluprint/class, tetapi berbeda dengan class
 * -> Interfce tidak bisa lansung dibuat sebagai object
 * Hanya bisa diturunkan
 *
 * Exception
 * tentang error yang dicegah dengan -> Throwable dengan keyword throw
 *
